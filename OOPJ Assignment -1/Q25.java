class Q25{


public static void main(String[] args)
{
     
       /* Write a program to convert state of Float instance into byte, 
      short, int, long, float and double.   */

       Float fts =1433.34f;


       byte b= fts.byteValue();
       System.out.println("The byte value of Float is b="+b);

       short s = fts.shortValue();
       System.out.println("The short value of Float is s="+s);

       int i = fts.intValue();
       System.out.println("The int value of Float is i="+i);

       long l = fts.longValue();
       System.out.println("The long value of Float is l="+l);

       float f = fts.floatValue();
       System.out.println("The float value of Float is f="+f);

       double d = fts.doubleValue();
       System.out.println("The double value of Float is d="+d);






}






}