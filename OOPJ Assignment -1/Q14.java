class Q14{
public static void main(String args [])
{

   /* Write a program to convert state of Short instance into she, 
      short, int, long, float and double.   */

      Short sh =14;


       byte b= sh.byteValue();
       System.out.println("The byte value of sh is b="+b);

       short s = sh.shortValue();
       System.out.println("The short value of sh is s="+s);

       int i = sh.intValue();
       System.out.println("The int value of sh is i="+i);

       long l = sh.longValue();
       System.out.println("The long value of sh is l="+l);

       float f = sh.floatValue();
       System.out.println("The float value of sh is f="+f);

       double d = sh.doubleValue();
       System.out.println("The double value of sh is d="+d);






}




}